def read_pdb(pdb_name):
    """Read a pdb file and return atom information with numpy array format.
    Args:
        pdb_name(str): The pdb file name, absolute path is suggested.
    Returns:
        atom_names(list): 1-dimension list contain all atom names in each residue.
        res_names(list): 1-dimension list of all residue names.
        res_ids(numpy.int32): Unique id for each residue names.
        crds(list): The list format of coordinates.
        res_pointer(numpy.int32): The pointer where the residue starts.
        flatten_atoms(numpy.str_): The flatten atom names.
        flatten_crds(numpy.float32): The numpy array format of coordinates.
        init_res_names(list): The residue name information of each atom.
        init_res_ids(list): The residue id of each atom.
    """
    with open(pdb_name, 'r') as pdb:
        lines = pdb.readlines()[1:]
    atom_names = []
    atom_group = []
    res_names = []
    res_ids = []
    init_res_names = []
    init_res_ids = []
    crds = []
    crd_group = []
    res_pointer = []
    flatten_atoms = []
    flatten_crds = []
    for index, line in enumerate(lines):
        if 'END' in line or 'TER' in line:
            atom_names.append(atom_group)
            crds.append(crd_group)
            continue
        atom_name = line[12:16].strip()
        res_name = line[17:20].strip()
        res_id = int(line[22:26].strip())
        crd = [float(line[30:38]),
               float(line[38:46]),
               float(line[46:54])]
        pointer = int(line[6:11].strip()) - 1
        flatten_atoms.append(atom_name)
        flatten_crds.append(crd)
        init_res_names.append(res_name)
        init_res_ids.append(res_id)
        if res_ids == []:
            res_ids.append(res_id)
            res_names.append(res_name)
            atom_group.append(atom_name)
            crd_group.append(crd)
            res_pointer.append(0)
        elif res_id != res_ids[-1]:
            atom_names.append(atom_group)
            crds.append(crd_group)
            atom_group = []
            crd_group = []
            res_ids.append(res_id)
            res_names.append(res_name)
            atom_group.append(atom_name)
            crd_group.append(crd)
            res_pointer.append(pointer)
        else:
            atom_group.append(atom_name)
            crd_group.append(crd)
        if index == len(lines) - 1:
            atom_names.append(atom_group)
            crds.append(crd_group)
    atom_names = atom_names
    res_names = res_names
    res_ids = np.array(res_ids, np.int32)
    crds = crds
    flatten_atoms = np.array(flatten_atoms, np.str_)
    flatten_crds = np.array(flatten_crds, np.float32)
    res_pointer = np.array(res_pointer, np.int32)
    return atom_names, res_names, res_ids, crds, res_pointer, flatten_atoms, flatten_crds, init_res_names, init_res_ids


def addH(pdb_name, save_pdb_name='test_addH_by_mindsponge.pdb'):
    """addH"""
    atom_names, res_names, res_ids, crds, res_pointer, flatten_atoms, flatten_crds, init_res_names, init_res_ids = \
        read_pdb(pdb_name)
    added_atom_names = None
    added_crd = None
    added_res_names = []
    added_res_ids = []
    if res_names[0] != 'ACE' and res_names[0] != 'NME':
        res_names[0] = 'N' + res_names[0]
    if res_names[-1] != 'ACE' and res_names[-1] != 'NME':
        res_names[-1] = 'C' + res_names[-1]
    for i, res_name in enumerate(res_names):
        tmp_atom_names = atom_names[i]
        tmp_crd = crds[i]
        mapping_index = np.where(np.array(list(amino_dict[res_name].keys())) == np.array(tmp_atom_names)[:, None])[-1]
        for j, c in enumerate(crd_dict[res_name]):
            if i > 0 and i < len(res_names) - 1:
                added_res_names.append(res_name)
            else:
                added_res_names.append(res_name[1:])
            added_res_ids.append(i + 1)
            if j in mapping_index:
                continue
            connected_atom = np.where(np.isin(bond_pairs[res_name], j))
            connected_bond = bond_pairs[res_name][connected_atom[0][0]]
            if connected_atom[1][0] == 0:
                reference_crd = crd_dict[res_name][connected_bond[1]]
            else:
                reference_crd = crd_dict[res_name][connected_bond[0]]
            tmp_crd = np.append(tmp_crd, (np.array(c) - np.array(reference_crd) + tmp_crd[0])[None, :], axis=0)
            tmp_atom_names = np.append(tmp_atom_names, list(amino_dict[res_name].keys())[j])
        if added_crd is None:
            added_crd = tmp_crd
            added_atom_names = tmp_atom_names
        else:
            added_crd = np.append(added_crd, tmp_crd, axis=0)
