def get_crds_from_file():
    """Get all the coordinates of residues from files.
    """
    import os
    file_path = '../data/aminos/'
    files = os.listdir(file_path)
    crd_dict = {}
    for file in files:
        if 'mol2' not in file:
            continue
        res = file.split('.')[0]
        with open(file_path + file, 'r') as f:
            lines = f.readlines()
        for i, line in enumerate(lines):
            if 'ATOM' in line:
                start_id = i + 1
            if 'BOND' in line:
                end_id = i
        crd = []
        for line in lines[start_id:end_id]:
            l = line.strip().split()
            crd.append([float(l[2]), float(l[3]), float(l[4])])
        crd_dict[res] = crd
    print(crd_dict)


